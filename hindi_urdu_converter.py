# Function to convert a string using a given mapping
def convert_text(text, mapping):
    result = ''
    i = 0
    while i < len(text):
        # Check for digraphs (like अं, अः in Devanagari)
        if i+1 < len(text) and text[i:i+2] in mapping:
            result += mapping[text[i:i+2]]
            i += 2
        elif text[i] in mapping:
            result += mapping[text[i]]
            i += 1
        else:
            result += text[i]  # Keep unknown chars as is
            i += 1
    return result

